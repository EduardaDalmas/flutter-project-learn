class GeneratorPage extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    var appState = context.watch<MyAppState>();
    var pair = appState.current;

    IconData icon = appState.favorites.contains(pair)
        ? Icons.favorite
        : Icons.favorite_border;

    return Center(
      child: Column(
        mainAxisAlignment: MainAxisAlignment.center,
        children: [
          BigCard(pair: pair),
          const SizedBox(height: 10),
          Row(
            mainAxisSize: MainAxisSize.min,
            children: [
              ElevatedButton.icon(
                onPressed: () {
                  appState.toggleFavorite();
                },
                icon: Icon(icon),
                label: const Text('Like'),
              ),
              const SizedBox(width: 10),
              ElevatedButton(
                onPressed: () {
                  appState.getNext();
                },
                child: const Text('Next'),
              ),
            ],
          ),
        ],
      ),
    );
  }
}

class BigCard extends StatelessWidget {
  final String pair;

  const BigCard({super.key, required this.pair});

  @override
  Widget build(BuildContext context) {
    return Card(
      elevation: 4,
      margin: const EdgeInsets.all(20),
      child: Padding(
        padding: const EdgeInsets.all(30),
        child: Text(
          pair,
          style: Theme.of(context).textTheme.headlineMedium,
        ),
      ),
    );
  }
}

// Classe que simula o estado da aplicação com uma lista de favoritos
class MyAppState extends ChangeNotifier {
  String _current = "Example";

  List<String> favorites = [];

  String get current => _current;

  void getNext() {
    _current = "New Example ${DateTime.now().second}";
    notifyListeners();
  }

  void toggleFavorite() {
    if (favorites.contains(_current)) {
      favorites.remove(_current);
    } else {
      favorites.add(_current);
    }
    notifyListeners();
  }

  
}
